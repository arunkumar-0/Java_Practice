public class While {
    public static void main(String[] args) {
        int i = 0 ; 

        //while loop 
        while(i<5){
            System.out.println("$");
            i++;
        }

        //do while 
        int j = 0;
        do {
            System.out.println("*");
            j++;
        }while(j<5);
    }
}
